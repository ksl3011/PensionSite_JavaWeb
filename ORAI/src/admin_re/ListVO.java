package admin_re;

import cmn.DTO;

public class ListVO extends DTO{
	private String pState; //결제여부
	private String payCode; //결제코드
	
	public ListVO(){}

	public ListVO(String pState, String payCode) {
		super();
		this.pState = pState;
		this.payCode = payCode;
	}

	@Override
	public String toString() {
		return "ListVO [pState=" + pState + ", payCode=" + payCode + "]";
	}

	public String getpState() {
		return pState;
	}

	public void setpState(String pState) {
		this.pState = pState;
	}

	public String getPayCode() {
		return payCode;
	}

	public void setPayCode(String payCode) {
		this.payCode = payCode;
	}
	
	
}
