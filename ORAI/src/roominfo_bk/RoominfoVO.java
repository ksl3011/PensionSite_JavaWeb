package roominfo_bk;

import cmn.DTO;

public class RoominfoVO extends DTO  {
	
	
	
	private String rcode;
	private String room;
	private int capacity;
	private int state;
	private int rnum;
	private String reg_dt;
	private String rinfo;
	private String pen_code;
	private int weekday ;
	private int weekend ;
	private int season  ;

	private String r_image;

	public RoominfoVO(){}

	public RoominfoVO(String rcode, String room, int capacity, int state, int rnum, String reg_dt, String rinfo,
			String pen_code, int weekday, int weekend, int season, String r_image) {
		super();
		this.rcode = rcode;
		this.room = room;
		this.capacity = capacity;
		this.state = state;
		this.rnum = rnum;
		this.reg_dt = reg_dt;
		this.rinfo = rinfo;
		this.pen_code = pen_code;
		this.r_image = r_image;
		this.weekday = weekday;
		this.weekend = weekend;
		this.season = season;
	}

	public String getRcode() {
		return rcode;
	}

	public void setRcode(String rcode) {
		this.rcode = rcode;
	}

	public String getRoom() {
		return room;
	}

	public void setRoom(String room) {
		this.room = room;
	}

	public int getCapacity() {
		return capacity;
	}

	public void setCapacity(int capacity) {
		this.capacity = capacity;
	}

	public int getState() {
		return state;
	}

	public void setState(int state) {
		this.state = state;
	}

	public int getRnum() {
		return rnum;
	}

	public void setRnum(int rnum) {
		this.rnum = rnum;
	}

	public String getReg_dt() {
		return reg_dt;
	}

	public void setReg_dt(String reg_dt) {
		this.reg_dt = reg_dt;
	}

	public String getRinfo() {
		return rinfo;
	}

	public void setRinfo(String rinfo) {
		this.rinfo = rinfo;
	}

	public String getPen_code() {
		return pen_code;
	}

	public void setPen_code(String pen_code) {
		this.pen_code = pen_code;
	}

	public String getR_image() {
		return r_image;
	}

	public void setR_image(String r_image) {
		this.r_image = r_image;
	}

	public int getWeekday() {
		return weekday;
	}

	public void setWeekday(int weekday) {
		this.weekday = weekday;
	}

	public int getWeekend() {
		return weekend;
	}

	public void setWeekend(int weekend) {
		this.weekend = weekend;
	}

	public int getSeason() {
		return season;
	}

	public void setSeason(int season) {
		this.season = season;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + capacity;
		result = prime * result + ((pen_code == null) ? 0 : pen_code.hashCode());
		result = prime * result + ((r_image == null) ? 0 : r_image.hashCode());
		result = prime * result + ((rcode == null) ? 0 : rcode.hashCode());
		result = prime * result + ((reg_dt == null) ? 0 : reg_dt.hashCode());
		result = prime * result + ((rinfo == null) ? 0 : rinfo.hashCode());
		result = prime * result + rnum;
		result = prime * result + ((room == null) ? 0 : room.hashCode());
		result = prime * result + season;
		result = prime * result + state;
		result = prime * result + weekday;
		result = prime * result + weekend;
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		RoominfoVO other = (RoominfoVO) obj;
		if (capacity != other.capacity)
			return false;
		if (pen_code == null) {
			if (other.pen_code != null)
				return false;
		} else if (!pen_code.equals(other.pen_code))
			return false;
		if (r_image == null) {
			if (other.r_image != null)
				return false;
		} else if (!r_image.equals(other.r_image))
			return false;
		if (rcode == null) {
			if (other.rcode != null)
				return false;
		} else if (!rcode.equals(other.rcode))
			return false;
		if (reg_dt == null) {
			if (other.reg_dt != null)
				return false;
		} else if (!reg_dt.equals(other.reg_dt))
			return false;
		if (rinfo == null) {
			if (other.rinfo != null)
				return false;
		} else if (!rinfo.equals(other.rinfo))
			return false;
		if (rnum != other.rnum)
			return false;
		if (room == null) {
			if (other.room != null)
				return false;
		} else if (!room.equals(other.room))
			return false;
		if (season != other.season)
			return false;
		if (state != other.state)
			return false;
		if (weekday != other.weekday)
			return false;
		if (weekend != other.weekend)
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "RoominfoVO [rcode=" + rcode + ", room=" + room + ", capacity=" + capacity + ", state=" + state
				+ ", rnum=" + rnum + ", reg_dt=" + reg_dt + ", rinfo=" + rinfo + ", pen_code=" + pen_code + ", r_image="
				+ r_image + ", weekday=" + weekday + ", weekend=" + weekend + ", season=" + season + "]";
	}

	

	

	

	
	
	
	
	

}
